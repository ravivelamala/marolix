# finding substring using rfind method 
# example 1 
sentence = "i will learn javascript"
substring_of_sentence = "a"
print(sentence.find(substring_of_sentence))

# example 2 

sentence = "i want to learn new skills"
substring_of_sentence = "l"
print(sentence.rfind(substring_of_sentence))

# example 3 
sentence = "i want to learn new skills"
substring_of_sentence = "z"
print(sentence.find(substring_of_sentence))